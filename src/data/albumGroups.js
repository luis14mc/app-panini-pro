// Metadatos por sección del álbum (bandera + código FIFA)
const flagByCode = worldCupGroups
  .flatMap((g) => g.teams)
  .reduce((acc, team) => {
    acc[team.code] = team.flag;
    return acc;
  }, { PANINI: '📘', FWC: '🏆' });

export function buildCountryMeta(sections, byCountry) {
  return sections.reduce((acc, section) => {
    const code = byCountry[section]?.[0]?.countryCode ?? section;
    acc[section] = { code, flag: flagByCode[code] ?? '🏳️' };
    return acc;
  }, {});
}
